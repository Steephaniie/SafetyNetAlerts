package com.safetynet.safetynetalerts.dto;

import java.util.List;


public class PersonInfoDTO {

    // Getters et setters
    private String firstName; // Prénom
    private String lastName;  // Nom
    private String address;   // Adresse
    private int age;          // Âge
    private String email;     // Email
    private List<String> medications; // Médicaments et posologie
    private List<String> allergies;   // Allergies

    // Constructeur avec tous les attributs
    public PersonInfoDTO(String firstName, String lastName, String address, int age, String email, List<String> medications, List<String> allergies) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.address = address;
        this.age = age;
        this.email = email;
        this.medications = medications;
        this.allergies = allergies;
    }

    // Getters et setters
    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public List<String> getMedications() {
        return medications;
    }

    public void setMedications(List<String> medications) {
        this.medications = medications;
    }

    public List<String> getAllergies() {
        return allergies;
    }

    public void setAllergies(List<String> allergies) {
        this.allergies = allergies;
    }
    
}
